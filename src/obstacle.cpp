Obstacle::Obstacle(Vector2 position)
{
    this -> position = position;

    for(unsigned int row = 0; row < grid.size(); ++row)
    {
        for (unsigned int column = 0; column < grid[0].size(); ++column)
        {
            if (grid[row][column] == 1)
            {
                float pos_x = position.x + column * 3; // calculates x-coordinate of the block based on the column number, 
                //since each block is 3 pixel wide we multiply it by 3 and add that to find correct x coordinate
                float pos_y = position.y + row * 3; // calculates y-coordinate of the block based on the row number
                Block block = Block({pos_x, pos_y});
                blocks.push_back(block);
            }
        }
    }
    
}

void Obstacle::Draw(){ // method that will darw all the blocks inside blocks vector --> range based for loop
    for(auto& block : blocks){
        block.Draw();
    }
}